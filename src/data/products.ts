function parseProduct(mainCategory: string, subcategory: string, rawName: string, index: number): Product {
  const id = `prod-${index + 1}`;
  const parenMatch = rawName.match(/\(([^)]+)\)/);
  let cleanName = rawName;
  let variations: string[] = [];

  if (parenMatch) {
    cleanName = rawName.replace(/\s*\([^)]+\)/g, "").trim();
    variations = parenMatch[1]
      .split(",")
      .map((v) => v.trim())
      .filter((v) => v.length > 0);
  }

  return {
    id,
    name: rawName,
    cleanName,
    mainCategory,
    subcategory,
    variations: variations.length > 0 ? variations : undefined,
  };
}

export const products: Product[] = rawCatalog.map(([mainCat, subCat, name], idx) => 
  parseProduct(mainCat, subCat, name, idx)
);
